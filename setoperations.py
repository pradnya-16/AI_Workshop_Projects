#Initializing the sets
E = {2,4,8,0,6}
N = {1,2,3,4,5}

#Union of sets
print("Union of E and N is:\n",E|N)

#Intersection of sets
print("Intersection of E and N is:\n",E&N)

#Difference of two sets
print("Difference of E and N is:\n",E-N)

#Symmentric difference of two sets
print("Symmetric difference of E and N is:\n",E^N)
